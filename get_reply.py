"""### 10 - Call Gemini"""

def call_openai(system_prompt, user_prompt):
    resp = client.responses.create(
        model="gpt-4",
        instructions=system_prompt,
        input=user_prompt,
        temperature=0.0,
        text={
            "format": {
                "type": "json_schema",
                "json_schema": response_schema,
                "strict": True
            }
        }
    )

    data = json.loads(resp.output_text)

    if "selections" not in data:
        raise ValueError("OpenAI response missing 'selections'")
    if len(data["selections"]) != 5:
        raise ValueError(f"Expected 5 selections, got {len(data['selections'])}")

    return data

