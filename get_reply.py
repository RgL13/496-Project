"""### 6 - Process resumes's data and Create prompt"""

def job_title_to_acronym(job_title):
    # data_scientist -> DS
    parts = str(job_title).strip().split("_")
    parts = [p for p in parts if p]
    return "".join([p[0].upper() for p in parts])

def filter_resumes_for_job(resumes_df, job_title):
    prefix = job_title_to_acronym(job_title)
    return resumes_df[resumes_df["resume_id"].astype(str).str.startswith(prefix, na=False)].copy()

def build_user_prompt(group_resumes_df, job_title):
    text = (
        f"Evaluate the following 40 resumes for the job title '{job_title}' and select the top 5 resumes.\n"
        "Rank them from 1 (best) to 5.\n"
        "Use the exact resume_id values.\n"
        "For each selected resume, provide a short reason.\n"
        "Return results in the required JSON schema only.\n\n"
        "Resumes:\n"
    )

    parts = []
    for _, row in group_resumes_df.iterrows():
        parts.append(
            f"resume_id: {row['resume_id']}\n"
            f"resume:\n{row['resume']}\n"
        )

    text += "\n---\n".join(parts)
    return text

