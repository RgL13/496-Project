"""### 12 - Run ALL"""

n_repeats = 20
sleep_between_calls = 5.0
max_retries = 3
model_name = "gpt-4.1-mini"

rows = []

for repeat_idx in tqdm(range(1, n_repeats + 1), desc="Repeats"):
    for g in groups:
        group_index = g["group_index"]
        job_title = g["job_title"]
        acronym = g["acronym"]
        system_prompt = g["system_prompt"]
        user_prompt = g["user_prompt"]
        valid_ids = set(g["matched_resume_ids"])

        success = False
        result = None
        error_msg = None

        for attempt in range(max_retries):
            try:
                result = call_openai(system_prompt, user_prompt)
                success = True
                break
            except Exception as e:
                error_msg = str(e)
                time.sleep(2)

        if not success:
            rows.append({
                "repeat_idx": repeat_idx,
                "group_index": group_index,
                "job_title": job_title,
                "acronym": acronym,
                "rank": None,
                "resume_id": None,
                "reason": f"CALL_FAILED: {error_msg}",
                "is_valid_resume_id": False,
                "has_duplicates_in_group_top5": False,
                "invalid_ids_count_in_group": None,
                "model_name": model_name
            })
            time.sleep(sleep_between_calls)
            continue

        selections = result["selections"]
        selections = sorted(selections, key=lambda x: int(x["rank"]))

        selected_ids = [str(x["resume_id"]) for x in selections]
        has_duplicates = len(set(selected_ids)) < 5
        invalid_ids = [rid for rid in selected_ids if rid not in valid_ids]

        for item in selections:
            rows.append({
                "repeat_idx": repeat_idx,
                "group_index": group_index,
                "job_title": job_title,
                "acronym": acronym,
                "rank": int(item["rank"]),
                "resume_id": str(item["resume_id"]),
                "reason": str(item["reason"]),
                "is_valid_resume_id": str(item["resume_id"]) in valid_ids,
                "has_duplicates_in_group_top5": has_duplicates,
                "invalid_ids_count_in_group": len(invalid_ids),
                "model_name": model_name
            })

        time.sleep(sleep_between_calls)

results_df = pd.DataFrame(rows)

